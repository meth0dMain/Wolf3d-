#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*dst1;
	unsigned char	*src1;

	dst1 = (unsigned char*)dst;
	src1 = (unsigned char*)src;
	if (dst1 < src1)
		return (ft_memcpy(dst, src, len));
	while (len-- > 0)
	{
		dst1[len] = src1[len];
	}
	return (dst);
}
