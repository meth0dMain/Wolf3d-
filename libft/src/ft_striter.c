#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t len;

	if (!s || !f)
		return ;
	len = ft_strlen(s);
	while (len > 0)
	{
		f(s);
		len--;
		s++;
	}
}
